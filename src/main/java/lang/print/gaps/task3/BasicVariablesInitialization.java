package lang.print.gaps.task3;

public class BasicVariablesInitialization {
    public static void main(String[] args) {
        //Declaring variables
        int first;
        int second;
        int third;

        //Assigning values
        first = 1;
        second = 10;
        third = 100;

        //print to console
        System.out.println(first + "\n" + second + "\n" + third);

    }
}
